    function drawVisualization() {
        drawPieChart();
        drawColChart();
    }

    function drawPieChart() {

        // Create the data table.
        var data = new google.visualization.DataTable();
        data.addColumn('string', 'Topping');
        data.addColumn('number', 'Slices');
        data.addRows([
          ['Mushrooms', 3],
          ['Onions', 1],
          ['Olives', 1],
          ['Zucchini', 1],
          ['Pepperoni', 2]
        ]);

        // Set chart options
        var options = {'title':'How Much Pizza I Ate Last Night',
                       'width':250,
                       'height':200};

        // Instantiate and draw our chart, passing in some options.
        var newsChart = new google.visualization.PieChart(document.getElementById('news-pie-chart'));
        newsChart.draw(data, options);
        var commentChart = new google.visualization.PieChart(document.getElementById('comment-pie-chart'));
        commentChart.draw(data, options);
    }

    function drawColChart() {
        var data = google.visualization.arrayToDataTable([
          ['Year', 'Sales', 'Expenses'],
          ['2004',  1000,      400],
          ['2005',  1170,      460],
          ['2006',  660,       1120],
          ['2007',  1030,      540]
        ]);

        var options = {
          title: 'Company Performance',
          hAxis: {title: 'Year', titleTextStyle: {color: 'red'}}
        };

        var newsChart = new google.visualization.ColumnChart(document.getElementById('news-col-chart'));
        newsChart.draw(data, options);
        var commentChart = new google.visualization.ColumnChart(document.getElementById('comment-col-chart'));
        commentChart.draw(data, options);
    }
